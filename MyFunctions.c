void selfShellSortAscending(int arr[], int size)
{
    int temp;
    for (int i = 0; i < size; i++)
    {
        for (int j = i + 1; j < size; j++)
        {
            if (arr[i] > arr[j])
            {
                temp = arr[i];
                arr[i] = arr[j];
                arr[j] = temp;
            }
        }
    }
}

void selfShellSortDescending(int arr[], int size)
{
    int temp;
    for (int i = 0; i < size; i++)
    {
        for (int j = i + 1; j < size; j++)
        {
            if (arr[i] < arr[j])
            {
                temp = arr[i];
                arr[i] = arr[j];
                arr[j] = temp;
            }
        }
    }
}

int circleCount(int *ptr, int a, int b)
{
    for (int i = 0; i < b; i++)
    {
        if (b <= a * i)
        {
            ptr[b + a - 1 - a * i] = 0;

            break;
        }
        else
        {
            continue;
        }
    }
}

int termTeller(int *arr, int a)
{
    int b = 0, i = 0;
    while (arr[i] != a)
    {
        b++;
        i++;
    }
    return b;
}

int matchTeller(char s1[], char s2[])
{
    for (int i = 0; i < strlen(s1); i++)
    {
        for (int j = 0; j < strlen(s2); j++)
        {
            if (s1[i] == s2[j])
            {
                return i + 1;
            }
        }
    }
    return -1;
}

void squeeze(char s1[], char s2[])
{
    int match = NO, k = 0;

    for (int i = 0; i < strlen(s1); i++)
    {
        for (int j = 0; j < strlen(s2); j++)
        {
            if (s1[i] == s2[j])
            {
                match = YES;
            }
        }

        if (match == NO)
        {
            s1[k++] = s1[i];
        }
        else
        {
            match = NO;
        }
    }

    s1[k] = '\0';
}

